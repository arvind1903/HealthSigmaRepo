package com.pos.hs.model;

import java.util.List;

public class Menus {
	private int menuId;

	private String MenuName;
	
	private String displayOrder;
	
	private String isChildAvailable;

	private List<PageAccess> pages;	
	

	public String getIsChildAvailable() {
		return isChildAvailable;
	}

	public void setIsChildAvailable(String isChildAvailable) {
		this.isChildAvailable = isChildAvailable;
	}

	public List<PageAccess> getPages() {
		return pages;
	}

	public void setPages(List<PageAccess> pages) {
		this.pages = pages;
	}

	

	public int getMenuId() {
		return menuId;
	}

	public void setMenuId(int menuId) {
		this.menuId = menuId;
	}

	public String getMenuName() {
		return MenuName;
	}

	public void setMenuName(String menuName) {
		MenuName = menuName;
	}

	public String getDisplayOrder() {
		return displayOrder;
	}

	public void setDisplayOrder(String displayOrder) {
		this.displayOrder = displayOrder;
	}

	@Override
	public String toString() {
		return "Menus [menuId=" + menuId + ", MenuName=" + MenuName + ", pages=" + pages + ", displayOrder="
				+ displayOrder + "]";
	}

}
