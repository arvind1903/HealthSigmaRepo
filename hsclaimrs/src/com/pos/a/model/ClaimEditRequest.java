package com.pos.a.model;



public class ClaimEditRequest {
	private ClaimHeader claimHeader;
	private String remarks;
public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
private ClaimLine claimLine;
private String newpredictedAmt;
private UserModel user;
public UserModel getUser() {
	return user;
}
public void setUser(UserModel user) {
	this.user = user;
}
private String comment;
 
public String getComment() {
	return comment;
}
public void setComment(String comment) {
	this.comment = comment;
}
public ClaimHeader getClaimHeader() {
	return claimHeader;
}
public void setClaimHeader(ClaimHeader claimHeader) {
	this.claimHeader = claimHeader;
}
@Override
public String toString() {
	return "ClaimEditRequest [claimHeader=" + claimHeader + ", claimLine=" + claimLine + ", newpredictedAmt="
			+ newpredictedAmt + ", comment=" + comment + "]";
}
public ClaimLine getClaimLine() {
	return claimLine;
}
public void setClaimLine(ClaimLine claimLine) {
	this.claimLine = claimLine;
}
public String getNewpredictedAmt() {
	return newpredictedAmt;
}
public void setNewpredictedAmt(String newpredictedAmt) {
	this.newpredictedAmt = newpredictedAmt;
}
}
